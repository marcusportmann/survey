-- -------------------------------------------------------------------------------------------------
-- CREATE SCHEMAS
-- -------------------------------------------------------------------------------------------------
CREATE SCHEMA SURVEY;



-- -------------------------------------------------------------------------------------------------
-- CREATE TABLES
-- -------------------------------------------------------------------------------------------------
CREATE TABLE SURVEY.SURVEY_TEMPLATES (
  ID           UUID NOT NULL,
  NAME         TEXT NOT NULL,
  DESCRIPTION  TEXT NOT NULL,

  PRIMARY KEY (ID)
);

COMMENT ON COLUMN SURVEY.SURVEY_TEMPLATES.ID
IS 'The Universally Unique Identifier (UUID) used to uniquely identify the survey template';

COMMENT ON COLUMN SURVEY.SURVEY_TEMPLATES.NAME
IS 'The name of the survey template.';

COMMENT ON COLUMN SURVEY.SURVEY_TEMPLATES.DESCRIPTION
IS 'The description for the survey template.';



CREATE TABLE SURVEY.SURVEY_TEMPLATE_GROUPS (
  ID                  UUID NOT NULL,
  SURVEY_TEMPLATE_ID  UUID NOT NULL,
  NAME                TEXT NOT NULL,
  DESCRIPTION         TEXT NOT NULL,

  PRIMARY KEY (ID),
  CONSTRAINT  SURVEY_SURVEY_TEMPLATE_GROUPS_SURVEY_TEMPLATE_FK FOREIGN KEY (SURVEY_TEMPLATE_ID) REFERENCES SURVEY.SURVEY_TEMPLATES(ID) ON DELETE CASCADE
);

CREATE INDEX SURVEY_SURVEY_TEMPLATE_GROUPS_SURVEY_TEMPLATE_ID_IX
  ON SURVEY.SURVEY_TEMPLATE_GROUPS
  (SURVEY_TEMPLATE_ID);

COMMENT ON COLUMN SURVEY.SURVEY_TEMPLATE_GROUPS.ID
IS 'The Universally Unique Identifier (UUID) used to uniquely identify the survey template group';

COMMENT ON COLUMN SURVEY.SURVEY_TEMPLATE_GROUPS.SURVEY_TEMPLATE_ID
IS 'The Universally Unique Identifier (UUID) used to uniquely identify the survey template this survey template group is associated with';

COMMENT ON COLUMN SURVEY.SURVEY_TEMPLATE_GROUPS.NAME
IS 'The name of the survey template group';

COMMENT ON COLUMN SURVEY.SURVEY_TEMPLATE_GROUPS.DESCRIPTION
IS 'The description for the survey template group';



CREATE TABLE SURVEY.SURVEY_TEMPLATE_GROUP_MEMBERS (
  ID                        UUID NOT NULL,
  SURVEY_TEMPLATE_GROUP_ID  UUID NOT NULL,
  NAME                      TEXT NOT NULL,

  PRIMARY KEY (ID),
  CONSTRAINT  SURVEY_SURVEY_TEMPLATE_GROUP_MEMBERS_SURVEY_TEMPLATE_GROUP_FK FOREIGN KEY (SURVEY_TEMPLATE_GROUP_ID) REFERENCES SURVEY.SURVEY_TEMPLATE_GROUPS(ID) ON DELETE CASCADE
);

CREATE INDEX SURVEY_SURVEY_TEMPLATE_GROUP_MEMBERS_SURVEY_TEMPLATE_GROUP_ID_IX
  ON SURVEY.SURVEY_TEMPLATE_GROUP_MEMBERS
  (SURVEY_TEMPLATE_GROUP_ID);

COMMENT ON COLUMN SURVEY.SURVEY_TEMPLATE_GROUP_MEMBERS.ID
IS 'The Universally Unique Identifier (UUID) used  to uniquely identify the survey template group member';

COMMENT ON COLUMN SURVEY.SURVEY_TEMPLATE_GROUP_MEMBERS.SURVEY_TEMPLATE_GROUP_ID
IS 'The Universally Unique Identifier (UUID) used to uniquely  identify the survey template group this survey template group member is associated with';

COMMENT ON COLUMN SURVEY.SURVEY_TEMPLATE_GROUP_MEMBERS.NAME
IS 'The name of the survey template group member';



CREATE TABLE SURVEY.SURVEY_TEMPLATE_GROUP_RATING_ITEMS (
  ID                        UUID NOT NULL,
  SURVEY_TEMPLATE_ID        UUID NOT NULL,
  NAME                      TEXT NOT NULL,
  SURVEY_TEMPLATE_GROUP_ID  UUID NOT NULL,
  RATING_TYPE               INTEGER NOT NULL,

  PRIMARY KEY (ID),
  CONSTRAINT  SURVEY_SURVEY_TEMPLATE_GROUP_RATING_ITEMS_SURVEY_TEMPLATE_FK FOREIGN KEY (SURVEY_TEMPLATE_ID) REFERENCES SURVEY.SURVEY_TEMPLATES(ID) ON DELETE CASCADE,
  CONSTRAINT  SURVEY_SURVEY_TEMPLATE_GROUP_RATING_ITEMS_SURVEY_TEMPLATE_GROUP_FK FOREIGN KEY (SURVEY_TEMPLATE_GROUP_ID) REFERENCES SURVEY.SURVEY_TEMPLATE_GROUPS(ID) ON DELETE CASCADE
);

CREATE INDEX SURVEY_SURVEY_TEMPLATE_GROUP_RATING_ITEMS_SURVEY_TEMPLATE_ID_IX
  ON SURVEY.SURVEY_TEMPLATE_GROUP_RATING_ITEMS
  (SURVEY_TEMPLATE_ID);

CREATE INDEX SURVEY_SURVEY_TEMPLATE_GROUP_RATING_ITEMS_SURVEY_TEMPLATE_GROUP_ID_IX
  ON SURVEY.SURVEY_TEMPLATE_GROUP_RATING_ITEMS
  (SURVEY_TEMPLATE_GROUP_ID);

COMMENT ON COLUMN SURVEY.SURVEY_TEMPLATE_GROUP_RATING_ITEMS.ID
IS 'The Universally Unique Identifier (UUID) used to uniquely identify the survey template group rating item';

COMMENT ON COLUMN SURVEY.SURVEY_TEMPLATE_GROUP_RATING_ITEMS.SURVEY_TEMPLATE_ID
IS 'The Universally Unique Identifier (UUID) used to uniquely identify the survey template this survey template group rating item is associated with';

COMMENT ON COLUMN SURVEY.SURVEY_TEMPLATE_GROUP_RATING_ITEMS.NAME
IS 'The name of the survey template group rating item';

COMMENT ON COLUMN SURVEY.SURVEY_TEMPLATE_GROUP_RATING_ITEMS.SURVEY_TEMPLATE_GROUP_ID
IS 'The Universally Unique Identifier (UUID) used to uniquely identify the survey template group this survey template group rating item is associated with';

COMMENT ON COLUMN SURVEY.SURVEY_TEMPLATE_GROUP_RATING_ITEMS.RATING_TYPE
IS 'The numeric code giving the type of survey template group rating e.g. 1 = Percentage, 2 = Yes/No/NA, etc';



CREATE TABLE SURVEY.SURVEY_REQUESTS (
  ID                  UUID NOT NULL,
  SURVEY_TEMPLATE_ID  UUID NOT NULL,

  PRIMARY KEY (ID),
  CONSTRAINT  SURVEY_SURVEY_REQUESTS_SURVEY_TEMPLATE_FK FOREIGN KEY (SURVEY_TEMPLATE_ID) REFERENCES SURVEY.SURVEY_TEMPLATES(ID) ON DELETE CASCADE
);

CREATE INDEX SURVEY_SURVEY_REQUESTS_SURVEY_TEMPLATE_ID_IX
  ON SURVEY.SURVEY_REQUESTS
  (SURVEY_TEMPLATE_ID);

COMMENT ON COLUMN SURVEY.SURVEY_REQUESTS.ID
IS 'The Universally Unique Identifier (UUID) used  to uniquely identify the survey template group member';

COMMENT ON COLUMN SURVEY.SURVEY_REQUESTS.SURVEY_TEMPLATE_ID
IS 'The Universally Unique Identifier (UUID) used to uniquely  identify the survey template group this survey template group member is associated with';



CREATE TABLE SURVEY.SURVEY_RESPONSES (
  ID                  UUID NOT NULL,
  SURVEY_TEMPLATE_ID  UUID NOT NULL,
  SURVEY_REQUEST_ID   UUID,

  PRIMARY KEY (ID),
  CONSTRAINT  SURVEY_SURVEY_RESPONSES_SURVEY_TEMPLATE_FK FOREIGN KEY (SURVEY_TEMPLATE_ID) REFERENCES SURVEY.SURVEY_TEMPLATES(ID) ON DELETE CASCADE,
  CONSTRAINT  SURVEY_SURVEY_RESPONSES_SURVEY_REQUEST_FK FOREIGN KEY (SURVEY_REQUEST_ID) REFERENCES SURVEY.SURVEY_REQUESTS(ID) ON DELETE CASCADE
);

CREATE INDEX SURVEY_SURVEY_RESPONSES_SURVEY_TEMPLATE_ID_IX
  ON SURVEY.SURVEY_RESPONSES
  (SURVEY_TEMPLATE_ID);

CREATE INDEX SURVEY_SURVEY_RESPONSES_SURVEY_REQUEST_ID_IX
  ON SURVEY.SURVEY_RESPONSES
  (SURVEY_REQUEST_ID);

COMMENT ON COLUMN SURVEY.SURVEY_RESPONSES.ID
IS 'The Universally Unique Identifier (UUID) used to uniquely identify the survey response';

COMMENT ON COLUMN SURVEY.SURVEY_RESPONSES.SURVEY_TEMPLATE_ID
IS 'The Universally Unique Identifier (UUID) used to uniquely identify the survey template the survey response is associated with';

COMMENT ON COLUMN SURVEY.SURVEY_RESPONSES.SURVEY_REQUEST_ID
IS 'The Universally Unique Identifier (UUID) used to uniquely identify the survey request the survey response is associated with';



CREATE TABLE SURVEY.SURVEY_RESPONSE_GROUP_RATING_ITEMS (
  ID                               UUID NOT NULL,
  SURVEY_RESPONSE_ID               UUID NOT NULL,
  SURVEY_TEMPLATE_ITEM_ID          UUID NOT NULL,
  SURVEY_TEMPLATE_GROUP_MEMBER_ID  UUID NOT NULL,
  RATING                           INTEGER NOT NULL,

  PRIMARY KEY (ID),
  CONSTRAINT  SURVEY_SURVEY_RESPONSE_GROUP_RATING_ITEMS_SURVEY_RESPONSE_FK FOREIGN KEY (SURVEY_RESPONSE_ID) REFERENCES SURVEY.SURVEY_RESPONSES(ID) ON DELETE CASCADE,
  CONSTRAINT  SURVEY_SURVEY_RESPONSE_GROUP_RATING_ITEMS_SURVEY_TEMPLATE_ITEM_FK FOREIGN KEY (SURVEY_TEMPLATE_ITEM_ID) REFERENCES SURVEY.SURVEY_TEMPLATE_GROUP_RATING_ITEMS(ID) ON DELETE CASCADE,
  CONSTRAINT  SURVEY_SURVEY_RESPONSE_GROUP_RATING_ITEMS_SURVEY_TEMPLATE_GROUP_MEMBER_FK FOREIGN KEY (SURVEY_TEMPLATE_GROUP_MEMBER_ID) REFERENCES SURVEY.SURVEY_TEMPLATE_GROUP_MEMBERS(ID) ON DELETE CASCADE
);

CREATE INDEX SURVEY_SURVEY_RESPONSE_GROUP_RATING_ITEMS_SURVEY_RESPONSE_ID_IX
  ON SURVEY.SURVEY_RESPONSE_GROUP_RATING_ITEMS
  (SURVEY_RESPONSE_ID);

CREATE INDEX SURVEY_SURVEY_RESPONSE_GROUP_RATING_ITEMS_SURVEY_TEMPLATE_ITEM_ID_IX
  ON SURVEY.SURVEY_RESPONSE_GROUP_RATING_ITEMS
  (SURVEY_TEMPLATE_ITEM_ID);

CREATE INDEX SURVEY_SURVEY_RESPONSE_GROUP_RATING_ITEMS_SURVEY_TEMPLATE_GROUP_MEMBER_ID_IX
  ON SURVEY.SURVEY_RESPONSE_GROUP_RATING_ITEMS
  (SURVEY_TEMPLATE_GROUP_MEMBER_ID);

COMMENT ON COLUMN SURVEY.SURVEY_RESPONSE_GROUP_RATING_ITEMS.ID
IS 'The Universally Unique Identifier (UUID) used to uniquely identify the survey response group rating  item';

COMMENT ON COLUMN SURVEY.SURVEY_RESPONSE_GROUP_RATING_ITEMS.SURVEY_RESPONSE_ID
IS 'The Universally Unique Identifier (UUID) used to uniquely identify the survey response this survey response group rating item is associated with';

COMMENT ON COLUMN SURVEY.SURVEY_RESPONSE_GROUP_RATING_ITEMS.SURVEY_TEMPLATE_ITEM_ID
IS 'The Universally Unique Identifier (UUID) used to uniquely identify the survey template group rating item this survey response group rating item is associated with';

COMMENT ON COLUMN SURVEY.SURVEY_RESPONSE_GROUP_RATING_ITEMS.SURVEY_TEMPLATE_GROUP_MEMBER_ID
IS 'The survey template group member this survey response group rating item is associated with';

COMMENT ON COLUMN SURVEY.SURVEY_RESPONSE_GROUP_RATING_ITEMS.RATING
IS 'The rating for the survey response group rating item e.g. 1=Yes, 0=No and -1=Not Applicable';



